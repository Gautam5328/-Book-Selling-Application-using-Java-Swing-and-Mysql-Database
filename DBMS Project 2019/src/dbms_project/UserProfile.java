    private void add_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_add_btnActionPerformed
        String isbn=isbn_tf.getText();
        String title=title_tf.getText();
        String author=author_tf.getText();
        String pub=pub_tf.getText();
        String cp=cp_tf.getText();
        String price=price_tf.getText();
        String desc=desc_ta.getText();
        List<String> genre=genre_list.getSelectedValuesList();
       
        if(isbn.isEmpty() || title.isEmpty() || author.isEmpty() || cp.isEmpty() || price.isEmpty())
          JOptionPane.showMessageDialog(null,"Fields empty!");
       else if(isbn.length()!=10)
           JOptionPane.showMessageDialog(null,"ISBN format invalid!");
       else 
       {
           if(db.checkBookDetails(userid,isbn))
           {
           if(db.insertUserBook(userid,isbn,desc,price,cp) && db.insertBookGenre(userid,isbn,genre) && db.insertBookDetails(isbn,title,author,pub))
           {
             
               JOptionPane.showMessageDialog(null, "Added book successfully");
               myBooksTab(model);
               myBooksTab(rmModel);
           }
           else
               JOptionPane.showMessageDialog(null,"Book already exists!");
           }
           else
           {
               if(db.insertUserBook(userid,isbn,desc,price,cp) && db.insertBookGenre(userid,isbn,genre))
           {
               JOptionPane.showMessageDialog(null, "Added book successfully");
               myBooksTab(model);
               myBooksTab(rmModel);
           }
           else
               JOptionPane.showMessageDialog(null,"Book already exists!");
           }
       }
           
    }//GEN-LAST:event_add_btnActionPerformed

