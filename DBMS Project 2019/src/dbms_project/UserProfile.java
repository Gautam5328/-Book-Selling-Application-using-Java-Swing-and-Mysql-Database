    private void accReq_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_accReq_btnActionPerformed
      
        int row=buyReqTable.getSelectedRow();
      if(row!=-1)
      {
          if(db.accReq(userid,buyReqTable.getValueAt(row,1),buyReqTable.getValueAt(row, 0),buyReqTable.getValueAt(row,3)))
          {
              buyReqModel.removeRow(row);
              JOptionPane.showMessageDialog(null,"Accepted Successfully");
              myBooksTab(model);
              myBooksTab(rmModel);
          }
          else
              JOptionPane.showMessageDialog(null,"Accept Error!");
      }
      else
          JOptionPane.showMessageDialog(null,"No row selected!");
    }//GEN-LAST:event_accReq_btnActionPerformed

