    private void search_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_search_btnActionPerformed
        if(searchby_cb.getSelectedItem().equals("Title"))
        {
            String title=titlesrch_tf.getText();
            searchModel.getDataVector().removeAllElements();
            if(!db.searchTitle(userid,title,searchModel))
                JOptionPane.showMessageDialog(null, "No results found!");
        }
        else
        {
            String genre=(String) genre_cb.getSelectedItem();
            searchModel.getDataVector().removeAllElements();
            if(!db.searchGenre(userid,genre,searchModel))
                JOptionPane.showMessageDialog(null, "No results found");
        }
    }//GEN-LAST:event_search_btnActionPerformed

